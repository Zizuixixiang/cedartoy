QUESTIONS = [
    {
        "id": question_id,
        "text": text,
        "options": [
            {"value": option_id, "text": option_text, "weight": weight}
            for option_id, (option_text, weight) in enumerate(options, 1)
        ],
    }
    for question_id, (text, options) in enumerate(_RAW, 1)
]


def get_questions(mode):
    if mode not in VALID_MODES:
        raise ValueError(f"unsupported mode: {mode}")
    return QUESTIONS


def is_fast_mode(mode):
    return mode == "full_fast"


def fast_batch_size(mode):
    return len(QUESTIONS) if is_fast_mode(mode) else 1


assert len(QUESTIONS) == 20
assert all(3 <= len(question["options"]) <= 4 for question in QUESTIONS)
assert sum(max(option["weight"] for option in question["options"]) for question in QUESTIONS) == 60
assert sum(min(option["weight"] for option in question["options"]) for question in QUESTIONS) == 0
