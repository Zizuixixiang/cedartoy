def score_answers(questions, answers):
    if len(questions) != 30 or len(answers) != 30:
        raise ValueError("love requires exactly 30 answers")
    scores = {dimension: 0 for dimension in DIMENSIONS}
    for question, answer in zip(questions, answers):
        option = next((item for item in question["options"] if item["value"] == answer), None)
        if option is None:
            raise ValueError(f"invalid answer {answer!r} for question {question['id']}")
        scores[option["dimension"]] += 1
    assert sum(scores.values()) == 30

    ranking = sorted(DIMENSIONS, key=lambda dimension: (-scores[dimension], dimension))
    highest = scores[ranking[0]]
    primary = [dimension for dimension in ranking if scores[dimension] == highest]
    lower_scores = sorted({score for score in scores.values() if score < highest}, reverse=True)
    secondary = (
        [dimension for dimension in ranking if scores[dimension] == lower_scores[0]]
        if lower_scores
        else []
    )
    lowest_score = min(scores.values())
    lowest = [dimension for dimension in DIMENSIONS if scores[dimension] == lowest_score]
    return {
        "result_value": "+".join(primary),
        "scores": scores,
        "ranking": ranking,
        "primary": primary,
        "secondary": secondary,
        "lowest": lowest,
    }


def _language_list(codes):
    return "、".join(f"{code}·{DIMENSIONS[code]}" for code in codes) or "无"


def reminder_for_scores(scores):
    lowest_score = min(scores.values())
    lowest_names = "、".join(
        DIMENSIONS[code] for code in DIMENSIONS if scores[code] == lowest_score
    )
    return (
        f"你最收不到的频道是 {lowest_names}。"
        "如果对方恰好爱用它表达，别怪 TA 没说——帮彼此翻译一下。"
    )


def format_result(mode, result):
    lines = [
        f"【爱之语测试完成 · {mode}模式】",
        "",
        "━━━ 五维分数 ━━━",
        *[f"{code}·{DIMENSIONS[code]}：{result['scores'][code]}/12" for code in DIMENSIONS],
        "",
        f"主语言：{_language_list(result['primary'])}",
        f"次语言：{_language_list(result['secondary'])}",
        "",
        "━━━ 主语言描述 ━━━",
    ]
    for code in result["primary"]:
        lines.extend([f"{code}·{DIMENSIONS[code]}：{DESCRIPTIONS[code]}", ""])
    if result.get("secondary"):
        lines.append("━━━ 次语言描述 ━━━")
        for code in result["secondary"]:
            lines.extend(
                [
                    f"{code}·{DIMENSIONS[code]}（次）：{SECONDARY_DESCRIPTIONS[code]}",
                    "",
                ]
            )
    lines.extend(
        [
            reminder_for_scores(result["scores"]),
            "",
            "（账号结果永久保留；游客结果存档 48 小时，可用 love_get_result 凭 player_id 查询。）",
        ]
    )
    return "\n".join(lines)


def format_stored_result(mode, result_value, detail, completed_at_label):
    result = {"result_value": result_value, **detail}
    text = format_result(mode, result).replace("爱之语测试完成", "爱之语历史结果", 1)
    return text.replace(
        "（账号结果永久保留；游客结果存档 48 小时，可用 love_get_result 凭 player_id 查询。）",
        f"完成时间：{completed_at_label}",
    )


def _person_data(player_id, result_value, detail):
    primary = list(detail.get("primary") or result_value.split("+"))
    return {
        "player_id": player_id,
        "scores": detail["scores"],
        "ranking": detail["ranking"],
        "primary": primary,
        "primary_names": [DIMENSIONS[code] for code in primary],
        "secondary": detail.get("secondary") or [],
    }


def _practice_advice_for(person):
    return [PRACTICES[code] for code in [*person["primary"], *person["secondary"]]]


def build_compare_data(player_id_a, result_a, detail_a, player_id_b, result_b, detail_b):
    a = _person_data(player_id_a, result_a, detail_a)
    b = _person_data(player_id_b, result_b, detail_b)
    if set(a["primary"]) & set(b["primary"]):
        relation = "同频"
    elif any(code in b["ranking"][:2] for code in a["primary"]):
        relation = "互译"
    elif any(code in b["ranking"][-2:] for code in a["primary"]):
        relation = "错频"
    else:
        # Five dimensions leave one middle rank; it is closer to mutual translation than wrong-frequency.
        relation = "互译"
    advice_for_a = _practice_advice_for(b)
    advice_for_b = _practice_advice_for(a)
    return {
        "kind": "love_compare",
        "player_a": a,
        "player_b": b,
        "relation": relation,
        "message": RELATION_MESSAGES[relation],
        "advice_for_a": advice_for_a,
        "advice_for_b": advice_for_b,
    }


def format_compare(data):
    a = data["player_a"]
    b = data["player_b"]
    name_a = a.get("display_name") or a["player_id"]
    name_b = b.get("display_name") or b["player_id"]
    score_line_a = " / ".join(f"{code} {a['scores'][code]}" for code in DIMENSIONS)
    score_line_b = " / ".join(f"{code} {b['scores'][code]}" for code in DIMENSIONS)
    return "\n".join(
        [
            "【爱之语双人对测】",
            "",
            f"A · {name_a}：{score_line_a}；主语言 {_language_list(a['primary'])}",
            f"B · {name_b}：{score_line_b}；主语言 {_language_list(b['primary'])}",
            "",
            f"关系：{data['relation']}——{data['message']}",
            "",
            f"给 {name_a} 的实践建议：{'；'.join(data['advice_for_a'])}",
            f"给 {name_b} 的实践建议：{'；'.join(data['advice_for_b'])}",
        ]
    )
